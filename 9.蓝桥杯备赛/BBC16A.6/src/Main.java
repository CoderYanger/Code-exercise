import java.util.Scanner;

public class Main {
    //拼好数
    /**
     * 计算单个数字中包含数字6的个数
     * @param x 输入的整数
     * @return 该整数中6的个数
     */
    public static int calc(int x) {
        int c = 0; // 初始化6的个数为0
        while (x != 0) { // 逐位拆解数字（直到数字被拆完）
            if (x % 10 == 6) { // 取当前最后一位，判断是否为6
                c++; // 是6则计数+1
            }
            x /= 10; // 去掉最后一位，继续处理前几位
        }
        return c; // 返回该数字中6的总个数
    }

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int n = scanner.nextInt(); // 输入数字的总个数
        int[] cnt = new int[10];  // cnt[i]：表示“含i个6”的数字的数量（i范围0-9，足够覆盖场景）
        int ret = 0; // 最终结果：能凑出的“好数”（总6数≥6）的组数

        // 第一步：遍历所有数字，统计“含6个数”的分布
        for (int i = 1; i <= n; i++) {
            int x = scanner.nextInt(); // 输入单个数字
            int t = calc(x); // 计算该数字中6的个数
            if (t >= 6) { // 如果单个数字的6的个数≥6，直接算一个好数
                ret++;
            } else { // 否则，统计到对应分类中
                cnt[t]++;
            }
        }

        // 第二步：贪心组合——按“高个数优先”的顺序组合，最大化组数（核心逻辑）
        // 优先级1：处理含5个6的数字（5需要搭配1个6，或两个5凑10≥6）
        if (cnt[5] > 0) {
            // 5+1=6：优先用5搭配含1个6的数字
            for (int i = 1; i <= 4 && cnt[5] > 0; i++) { // i=1时优先，i=2/3/4也可（5+2=7≥6、5+3=8≥6、5+4=9≥6）
                if (cnt[i] <= cnt[5]) { // 含i个6的数字更少，全部搭配完
                    ret += cnt[i]; // 每对组合算一个好数
                    cnt[5] -= cnt[i]; // 消耗对应数量的5
                    cnt[i] = 0; // i类数字用完
                } else { // 含i个6的数字更多，用完所有5
                    ret += cnt[5]; // 每对组合算一个好数
                    cnt[i] -= cnt[5]; // 消耗对应数量的i类数字
                    cnt[5] = 0; // 5类数字用完
                }
            }
            // 剩余的5：5+5=10≥6，每两个5凑一个好数
            ret += cnt[5] / 2;
            cnt[5] %= 2; // 剩下的单个5无法再凑，后续无作用
        }

        // 优先级2：处理含4个6的数字（4需要搭配2个6，或两个4凑8≥6）
        if (cnt[4] > 0) {
            // 4+1+1=6：优先用4搭配两个1（最节省高个数数字）
            if (2 * cnt[4] <= cnt[1]) { // 1的数量足够搭配所有4
                ret += cnt[4]; // 每个4配两个1，算一个好数
                cnt[1] -= 2 * cnt[4]; // 消耗对应数量的1
                cnt[4] = 0; // 4类数字用完
            } else { // 1的数量不足，先用完1
                ret += cnt[1] / 2; // 每两个1配一个4，算一个好数
                cnt[4] -= cnt[1] / 2; // 消耗对应数量的4
                cnt[1] %= 2; // 剩余单个1无法再配4
            }
            // 剩余的4：优先搭配2/3（4+2=6、4+3=7≥6）
            for (int i = 2; i <= 3 && cnt[4] > 0; i++) {
                if (cnt[i] <= cnt[4]) { // i类数字更少，全部搭配完
                    ret += cnt[i];
                    cnt[4] -= cnt[i];
                    cnt[i] = 0;
                } else { // i类数字更多，用完所有4
                    ret += cnt[4];
                    cnt[i] -= cnt[4];
                    cnt[4] = 0;
                }
            }
            // 剩余的4：4+4=8≥6，每两个4凑一个好数
            ret += cnt[4] / 2;
            cnt[4] %= 2; // 剩余单个4无法再凑
        }

        // 优先级3：处理含3个6的数字（3需要搭配1+2=3，或两个3凑6）
        if (cnt[3] > 0) {
            // 3+2+1=6：优先用3搭配1和2（各一个）
            int t = Math.min(Math.min(cnt[1], cnt[2]), cnt[3]); // 取三者最小值（能凑的组数）
            ret += t; // 每一组算一个好数
            cnt[1] -= t; // 消耗对应数量的1
            cnt[2] -= t; // 消耗对应数量的2
            cnt[3] -= t; // 消耗对应数量的3
        }
        // 剩余的3：3+3=6，每两个3凑一个好数
        ret += cnt[3] / 2;
        cnt[3] %= 2; // 剩余单个3
        cnt[2] += cnt[3]; // 单个3无法凑，合并到2类（后续按2类处理）

        // 优先级4：处理含2个6的数字（2+2+2=6，每三个2凑一个好数）
        ret += cnt[2] / 3;

        // 输出最终结果
        System.out.println(ret);
        scanner.close();
    }
}
